class Categoias {
    
    constructor(nombre, colorCard, colorTexto) {

        this.nombre = nombre;

        this.colorCard = colorCard;

        this.colorTexto = colorTexto;

    }

    getNombre() {
        return this.nombre;
    }

    getColorCard() {
        return this.colorCard;
    }

    getColorTexto() {
        return this.colorTexto;
    }


}

